package Assign;

/**
 * 2 Dimension Read Excel
 *
 */
public class ReadExcelTest3 {
	
	 public static void main(String[] args) 
	    { 
	  
	        int[][] arr = { { 1, 2 }, { 3, 4 } }; /* 2 Dimsnal Array*/
	  
	        for (int i = 0; i < 2; i++) { 
	            for (int j = 0; j < 2; j++) {    /* Nested For Loop */
	                System.out.print(arr[i][j] + " "); 
	            } 
	  
	            System.out.println(); 
	        } 
	    } 
	

}
